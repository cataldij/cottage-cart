interface SocialLinksProps {
  conference: {
    social_twitter?: string | null
    social_linkedin?: string | null
    social_instagram?: string | null
    social_youtube?: string | null
    social_facebook?: string | null
    social_tiktok?: string | null
    social_discord?: string | null
    social_slack?: string | null
    primary_color?: string | null
  }
}

export function SocialLinks({ conference }: SocialLinksProps) {
  const links = [
    { url: conference.social_twitter, icon: Twitter, label: 'Twitter' },
    { url: conference.social_linkedin, icon: Linkedin, label: 'LinkedIn' },
    { url: conference.social_instagram, icon: Instagram, label: 'Instagram' },
    { url: conference.social_youtube, icon: Youtube, label: 'YouTube' },
    { url: conference.social_facebook, icon: Facebook, label: 'Facebook' },
    { url: conference.social_tiktok, icon: TikTokIcon, label: 'TikTok' },
    { url: conference.social_discord, icon: DiscordIcon, label: 'Discord' },
    { url: conference.social_slack, icon: SlackIcon, label: 'Slack' },
  ].filter((link) => link.url)

  if (links.length === 0) return null

  return (
    <div className="flex justify-center gap-4">
      {links.map(({ url, icon: Icon, label }) => (
        <a
          key={label}
          href={url!}
          target="_blank"
          rel="noopener noreferrer"
          className="p-2 rounded-full transition-colors hover:bg-muted"
          style={{ color: conference.primary_color || '#2563eb' }}
          title={label}
        >
          <Icon className="h-5 w-5" />
        </a>
      ))}
    </div>
  )
}
